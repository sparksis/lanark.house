#!/usr/bin/env node
/**
 * Generates src/lib/content-manifest.ts from content/ MDX files.
 *
 * Runs as `prebuild` so the manifest is available to Next.js (webpack or Turbopack).
 *
 * Produces two exports per content type:
 *   BUILDER_RAW  / POST_RAW  — raw MDX strings (for frontmatter parsing)
 *   BUILDER_HTML / POST_HTML — compiled HTML (avoids new Function() in Workers)
 *
 * Cloudflare Workers block new Function() / eval(), so compileMDX from
 * next-mdx-remote/rsc cannot run at runtime. HTML is pre-compiled here using
 * remark + remark-rehype + rehype-stringify (pure Node.js, no eval).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";
import { unified } from "unified";
import remarkParse from "remark-parse";
import remarkMdx from "remark-mdx";
import remarkGfm from "remark-gfm";
import remarkRehype from "remark-rehype";
import rehypeStringify from "rehype-stringify";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const CONTENT = path.join(ROOT, "content");
const OUT = path.join(ROOT, "src", "lib", "content-manifest.ts");

/** Compile MDX body (frontmatter already stripped) to HTML */
async function mdxToHtml(body) {
  const file = await unified()
    .use(remarkParse)
    .use(remarkMdx)
    .use(remarkGfm)
    .use(remarkRehype, { allowDangerousHtml: true })
    .use(rehypeStringify, { allowDangerousHtml: true })
    .process(body);
  return String(file);
}

const lines = [
  "// AUTO-GENERATED by scripts/generate-manifest.mjs — do not edit manually",
  "// Re-generated automatically before each build via the `prebuild` npm script",
  "",
];

// ─── Builders ────────────────────────────────────────────────────────────────
const builderDir = path.join(CONTENT, "builders");
const builderFiles = fs.readdirSync(builderDir).filter((f) => f.endsWith(".mdx")).sort();

const builderRaw = {};
const builderHtml = {};

for (const file of builderFiles) {
  const slug = path.basename(file, ".mdx");
  const raw = fs.readFileSync(path.join(builderDir, file), "utf-8");
  const { content } = matter(raw);
  builderRaw[slug] = raw;
  builderHtml[slug] = await mdxToHtml(content);
}

lines.push("export const BUILDER_RAW: Record<string, string> = {");
for (const [slug, raw] of Object.entries(builderRaw)) {
  lines.push(`  ${JSON.stringify(slug)}: ${JSON.stringify(raw)},`);
}
lines.push("};");
lines.push("");

lines.push("export const BUILDER_HTML: Record<string, string> = {");
for (const [slug, html] of Object.entries(builderHtml)) {
  lines.push(`  ${JSON.stringify(slug)}: ${JSON.stringify(html)},`);
}
lines.push("};");
lines.push("");

// ─── Posts ───────────────────────────────────────────────────────────────────
const postDir = path.join(CONTENT, "posts");
const subdirs = fs.readdirSync(postDir)
  .filter((d) => fs.statSync(path.join(postDir, d)).isDirectory())
  .sort();

const postRaw = {};
const postHtml = {};

for (const subdir of subdirs) {
  const files = fs.readdirSync(path.join(postDir, subdir))
    .filter((f) => f.endsWith(".mdx"))
    .sort();
  for (const file of files) {
    const key = `${subdir}/${path.basename(file, ".mdx")}`;
    const raw = fs.readFileSync(path.join(postDir, subdir, file), "utf-8");
    const { content } = matter(raw);
    postRaw[key] = raw;
    postHtml[key] = await mdxToHtml(content);
  }
}

lines.push("export const POST_RAW: Record<string, string> = {");
for (const [key, raw] of Object.entries(postRaw)) {
  lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(raw)},`);
}
lines.push("};");
lines.push("");

lines.push("export const POST_HTML: Record<string, string> = {");
for (const [key, html] of Object.entries(postHtml)) {
  lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(html)},`);
}
lines.push("};");
lines.push("");

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, lines.join("\n"));

const postCount = Object.keys(postRaw).length;
console.log(`✓ content-manifest.ts — ${builderFiles.length} builders, ${postCount} posts (raw + HTML)`);
